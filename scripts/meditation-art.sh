#!/bin/bash
# meditation-art.sh - 瞑想を促すバナーを表示する
#
# 使用方法:
#   meditation-art.sh
#
# 環境変数:
#   COLUMNS - ターミナル幅（省略時は80）
#
# 動作:
#   - ターミナル幅が80文字以上: FIGletスタイルのバナーを表示
#   - ターミナル幅が80文字未満: シンプルなメッセージを表示

# ターミナル幅を取得（未定義の場合は80）
terminal_width="${COLUMNS:-80}"

# 最小幅の定義（FIGletバナー表示に必要な幅）
min_width=80

if [[ "$terminal_width" -ge "$min_width" ]]; then
    cat << 'EOF'

          ╭────────────────────────────────────────╮
          │ 瞑想するか、やりだしてから納得するか。 │
          │   いいから落ち着いてコードを読もう。   │
        ○ ╰────────────────────────────────────────╯
    ʕ◔ϖ◔ʔ
EOF
else
    # 狭いターミナル用のシンプルなメッセージ
    echo ""
    echo "瞑想するか、やりだしてから納得するか"
    echo ""
fi
